import board


def pin_number_to_digital_gpio(pin):
    """

    :param pin:
    :type pin: int
    :return:
    """

    if pin == 0:
        return board.D0
    elif pin == 1:
        return board.D1
    elif pin == 2:
        return board.D2
    elif pin == 3:
        return board.D3
    elif pin == 4:
        return board.D4
    elif pin == 5:
        return board.D5
    elif pin == 6:
        return board.D6
    elif pin == 7:
        return board.D7
    elif pin == 8:
        return board.D8
    elif pin == 9:
        return board.D9
    elif pin == 10:
        return board.D10
    elif pin == 11:
        return board.D11
    elif pin == 12:
        return board.D12
    elif pin == 13:
        return board.D13
    elif pin == 14:
        return board.D14
    elif pin == 15:
        return board.D15
    elif pin == 16:
        return board.D16
    elif pin == 17:
        return board.D17
    elif pin == 18:
        return board.D18
    elif pin == 19:
        return board.D19
    elif pin == 20:
        return board.D20
    elif pin == 21:
        return board.D21
    elif pin == 22:
        return board.D22
    elif pin == 23:
        return board.D23
    elif pin == 24:
        return board.D24
